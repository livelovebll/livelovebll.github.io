def addition(value1, value2):
    return value1 + value2

def substraction(value1, value2):
    return value1 - value2

def multiplication(value1, value2):
    return value1 * value2

def division(value1, value2):
    return value1 / value2

print(addition(1, 3))
print(substraction(7, 1))
print(multiplication(2, 2))
print(division(10, 2))